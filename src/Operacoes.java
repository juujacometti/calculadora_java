public class Operacoes {

// Soma:
    public static double somar(double x, double y){
        return x + y;
    }

// Subtração:

    public static double subt(double x, double y){
        return x - y;
    }

// Multiplicação:

    public static double mult(double x, double y){
        return x * y;
    }

// Divisão:

    public static double divi(double x, double y){
        return x / y;
    }


}
